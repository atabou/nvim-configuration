
local set = vim.opt

set.number = true -- Sets the number line on the left

set.expandtab = true
set.smarttab = true
set.shiftwidth = 4
set.tabstop = 4

set.hlsearch = true
set.incsearch = true
set.ignorecase = true
set.smartcase = true

set.splitbelow = true -- When horizontal split occurs it is opened to the right
set.splitright = true -- When vertical split occurs it is opened to the right
set.wrap = false -- When text reaches the end of the window it continues on a new line if set to true.
set.scrolloff = 5
set.fileencoding = 'utf-8'
set.termguicolors = true
set.background="dark"
set.signcolumn="yes"

set.cursorline = true -- Sets a light color over the whole row the cursor in on.

set.hidden = true

set.mouse = 'a' -- Allows the use of the mouse in the vim instances
